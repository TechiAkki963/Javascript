// With For Loop

for (let i = 1; i <= 100; i++) {
  if (i % 3 === 0 && i % 5 === 0) {
    console.log(`FizzBuzz`);
  } else if (i % 5 === 0) {
    console.log(`Buzz`);
  } else if (i % 3 === 0) {
    console.log(`Fizz`);
  } else {
    console.log(i);
  }
}

// With While Loop

let j = 1;
while (j <= 100) {
  if (i % 3 === 0 && i % 5 === 0) {
    console.log(`FizzBuzz`);
  } else if (i % 5 === 0) {
    console.log(`Buzz`);
  } else if (i % 3 === 0) {
    console.log(`Fizz`);
  } else {
    console.log(i);
  }
  j++;
}
